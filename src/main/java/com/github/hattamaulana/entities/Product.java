package com.github.hattamaulana.entities;

/**
 *
 * @author ngub
 */
public class Product {
    private String id;
    private String nama;
    private int harga;
    private String jenis;

    public Product() {}

    public Product(String nama, String jenis, int harga) {
        this.jenis = jenis;
        this.nama = nama;
        this.harga = harga;
    }

    public String getJenis() {
        return jenis;
    }

    public void setJenis(String jenis) {
        this.jenis = jenis;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public int getHarga() {
        return harga;
    }

    public void setHarga(int harga) {
        this.harga = harga;
    }
    
    
}
